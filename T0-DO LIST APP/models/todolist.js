const mongoose = require("mongoose");
const listSchema = mongoose.Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
  },
});

const list = mongoose.model("TodoList", listSchema);
module.exports = list;
